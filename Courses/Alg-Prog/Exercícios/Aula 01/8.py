"""
Escreva um programa que leia três números inteiros a, b e c,
calcule a  b + c e mostre o resultado na saída padrão para o(a)
usuário(a). Faça a simulação passo a passo da execução do
programa.
"""

a = int(input('Numero 1: '))
b = int(input('Numero 2:'))
c = int(input('Numero 3: '))

calc = a * b + c

print(f'{a} * {b} + {c} é igual a {calc}')

exit(0)