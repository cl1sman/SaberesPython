"""
9. Escreva um programa que leia um número inteiro e mostre o seu
quadrado e seu cubo. Por exemplo, se o número de entrada é 3, a
saída deve ser 9 e 27. Faça a simulação passo a passo da
execução do programa.
"""

numero = int(input('Informe um número: '))

