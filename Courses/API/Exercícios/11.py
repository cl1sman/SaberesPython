#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
Escreva um programa que leia um número inteiro e mostre o
resultado do quociente da divisão desse número por 2 e por 3.
Faça a simulação passo a passo da execução do programa.
"""

numero = int(input('Entre com um número inteiro: '))
quociente_por_2 = numero / 2
quociente_por_3 = numero / 3

print(f'O quociente de {numero} dividido por 2 e por 3, respectivamente é: {quociente_por_2}, {quociente_por_3} ')
exit(0)