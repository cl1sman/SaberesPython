"""
Leia duas strings no console do Python e faça testes com os
operadores aritméticos e relacionais, funções e fatias (slices).
"""
string1 = input()
string2 = input()

if string1 == string2:
    print('igual a')

if string1 != string2:
    print('diferente de')

if string1 < string2:
    print('menor que')

if string1 > string2:
    print('maior que')

if string1 <= string2:
    print('menor que ou igual a')

if string1 >= string2:
    print('maior que ou igual a')



exit(0)