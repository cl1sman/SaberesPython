dog_breeds = ['french_bulldog', 'dalmation', 'shihtzu', 'poodle', 'collie']

for breed in dog_breeds:
    print(breed)
"""
    The general way of writing a for loop is:
        for <temporary variable> in <list variable>:
            <action>
"""
"""
    In our dog breeds example, breed was the temporary variable,
    dog_breeds was the list variable, and print(breed) was the 
    action performed on every item in the list.
"""

